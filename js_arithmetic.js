//javascript arithmetic operators

// + = addition
// - = subtraction
// * = multiplication
// / = divide
// ** = exponentiation
// % = modulus (reminder)
// ++ = increment
// -- = decrement

let x = 100 + 50;
console.log(x);

let a = 10, b = 20;
x = a + b;
console.log(x);

x = (100 + 50) * a;
console.log(x);

//subtraction
x = b - a;
console.log(x);

//multiplication
x = a * b;
console.log(x);

//dividing
x = b / a;
console.log(x);

//reminder
x =  a % 6;
console.log(x);

//increment
console.log(`x is:`, x);
x++;
console.log(`x ++: `,x);

//decrement
console.log(`x is:`, x);
x--;
console.log(`x -- :`, x);

//exponentiation
console.log(`x ** x: `, x*x);

